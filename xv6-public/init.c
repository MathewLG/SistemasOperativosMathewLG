// init: The initial user-level program

#include "types.h"
#include "stat.h"
#include "user.h"
#include "fcntl.h"

char *argv[] = { "sh", 0 };

int
main(void)
{
  int pid, wpid;

  if(open("console", O_RDWR) < 0){
    mknod("console", 1, 1);
    open("console", O_RDWR);
  }
  dup(0);  // stdout
  dup(0);  // stderr

  for(;;){
    printf(1, "init: Amarren sus cinturones porque está arrancando el sistema operativo más perrón!\n");
    pid = fork();
    if(pid < 0){
      printf(1, "init: fork failed\n");
      exit();
    }
    if(pid == 0){
      exec("sh", argv);
		//Cuando sustituyo el argumento por "ls" al arrancar, el sistema operativo lista todos los archivos que contiene
		//Cuando sustituyo por "no programa" se despliega reiteradamente un mensaje de error que dice init: exec failed
		//Cuando vuelvo a poner sh y modifico el mensaje, vuelve a la normalidad y despliega mi mensaje. 
      printf(1, "init: exec sh failed\n");
      exit();
    }
    while((wpid=wait()) >= 0 && wpid != pid)
      printf(1, "zombie!\n");
  }
}
